    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $req)
    {
        $this->validate($req, [
            'image' => 'required',
            'name' => 'required',
            'rating' => 'required',
            'ulasan' => 'required',
        ]);

        $img = $req->file('image');        
        $ext = $img->getClientOriginalExtension();
        $dir = "upload/";
        $name = uniqid().'.'.$ext;
        $upload = $img->move($dir, $name);

        $input = $req->all();
        $input['image'] = $dir.$name;        
        $ulasan = new Ulasan($input);        
        $ulasan->save();

        return back();
    }

