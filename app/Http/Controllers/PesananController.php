    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $req)
    {
        $gunung = Gunung::find($req->gunung_id);
        $input = $req->all();
        $input['total_harga'] = $gunung->harga * $req->jml_peserta;
        $pesanan = new Pesanan($input);
        $pesanan->save();

        $gunung->jml_pesanan++;
        $gunung->save();

        return back();
    }

